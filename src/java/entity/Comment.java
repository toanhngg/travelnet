package entity;

/**
 *
 * @author admin
 */
public class Comment {

    private int cid;
    private String ctitle;
    private User user;
    private Tour tour;

    public Comment() {
    }

    public Comment(int cid, String ctitle, User user, Tour tour) {
        this.cid = cid;
        this.ctitle = ctitle;
        this.user = user;
        this.tour = tour;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCtitle() {
        return ctitle;
    }

    public void setCtitle(String ctitle) {
        this.ctitle = ctitle;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Tour getTour() {
        return tour;
    }

    public void setTour(Tour tour) {
        this.tour = tour;
    }

 

}
